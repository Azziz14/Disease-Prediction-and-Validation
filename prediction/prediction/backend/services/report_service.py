"""
Health Report Generator
Produces structured AI-generated clinical decision support reports.
"""

import uuid
from datetime import datetime


class ReportService:
    
    FEATURE_NAMES = [
        "Pregnancies", "Glucose", "Blood Pressure", "Skin Thickness",
        "Insulin", "BMI", "Diabetes Pedigree Function", "Age"
    ]
    
    NORMAL_RANGES = {
        "Glucose": (70, 140, "mg/dL"),
        "Blood Pressure": (60, 120, "mmHg"),
        "BMI": (18.5, 25.0, "kg/m²"),
        "Insulin": (16, 166, "μU/mL"),
        "Age": (0, 120, "years"),
        "Skin Thickness": (10, 50, "mm"),
        "Pregnancies": (0, 15, "count"),
        "Diabetes Pedigree Function": (0.0, 2.5, "index")
    }

    def generate_report(self, prediction_result: dict, features: list, prescription: str) -> dict:
        """
        Generate a comprehensive health report from prediction results.
        """
        report_id = f"RPT-{uuid.uuid4().hex[:8].upper()}"
        timestamp = datetime.now().isoformat()

        # Feature analysis
        feature_analysis = self._analyze_features(features)

        # Risk summary
        risk = prediction_result.get("risk", "Unknown")
        confidence = prediction_result.get("confidence", 0)
        disease = prediction_result.get("disease", "diabetes")

        # Build report
        report = {
            "report_id": report_id,
            "generated_at": timestamp,
            "summary": {
                "disease_context": disease.capitalize(),
                "risk_level": risk,
                "confidence_score": round(confidence * 100, 1),
                "overall_assessment": self._get_assessment_text(risk, confidence)
            },
            "feature_analysis": feature_analysis,
            "risk_factors": prediction_result.get("explanation", {}),
            "drug_report": {
                "prescribed": prescription,
                "validated_drugs": prediction_result.get("matched_drugs", []),
                "suggestions": prediction_result.get("suggestions", []),
                "interactions_detected": prediction_result.get("drug_interactions", []),
                "interaction_severity": "High" if prediction_result.get("drug_interactions") else "None"
            },
            "recommendations": self._generate_recommendations(risk, features, disease),
            "disclaimer": (
                "MEDICAL DISCLAIMER: This report is generated by an AI-based clinical decision "
                "support system and is intended for informational purposes only. It does not "
                "constitute medical advice, diagnosis, or treatment. Always consult with a "
                "qualified healthcare professional before making any medical decisions. "
                "AI predictions are probabilistic and should be interpreted alongside "
                "clinical judgment and additional diagnostic tests."
            )
        }

        return report

    def _analyze_features(self, features: list) -> list:
        """Analyze each feature against normal ranges."""
        analysis = []
        for i, value in enumerate(features):
            name = self.FEATURE_NAMES[i] if i < len(self.FEATURE_NAMES) else f"Feature_{i}"
            ranges = self.NORMAL_RANGES.get(name, (0, 999, ""))
            low, high, unit = ranges

            status = "Normal"
            if value < low:
                status = "Below Normal"
            elif value > high:
                status = "Above Normal"

            analysis.append({
                "name": name,
                "value": value,
                "unit": unit,
                "normal_range": f"{low} - {high}",
                "status": status
            })

        return analysis

    def _get_assessment_text(self, risk: str, confidence: float) -> str:
        conf_pct = round(confidence * 100, 1)
        if risk == "High":
            return (
                f"The AI model has identified a HIGH risk pattern with {conf_pct}% confidence. "
                "Multiple risk factors contribute to this assessment. Immediate clinical "
                "review is strongly recommended."
            )
        elif risk == "Moderate":
            return (
                f"The AI model has identified a MODERATE risk pattern with {conf_pct}% confidence. "
                "Some risk factors are elevated. Preventive measures and follow-up monitoring "
                "are recommended."
            )
        else:
            return (
                f"The AI model has identified a LOW risk pattern with {conf_pct}% confidence. "
                "Most metrics fall within normal ranges. Continue maintaining healthy habits "
                "and schedule routine check-ups."
            )

    def _generate_recommendations(self, risk: str, features: list, disease: str) -> list:
        recs = []

        glucose = features[1] if len(features) > 1 else 0
        bmi = features[5] if len(features) > 5 else 0
        bp = features[2] if len(features) > 2 else 0
        age = features[7] if len(features) > 7 else 0

        if risk in ["High", "Moderate"]:
            recs.append("Schedule a follow-up appointment with your primary care physician within 2 weeks.")

        if glucose > 140:
            recs.append("Glucose levels are elevated. Consider HbA1c testing and dietary modifications.")
        if glucose > 200:
            recs.append("CRITICAL: Glucose severely elevated. Immediate medical attention recommended.")

        if bmi > 30:
            recs.append("BMI indicates obesity. A structured weight management program is recommended.")
        elif bmi > 25:
            recs.append("BMI indicates overweight. Consider dietary counseling and increased physical activity.")

        if bp > 140:
            recs.append("Blood pressure is elevated. Consider antihypertensive evaluation.")

        if age > 45:
            recs.append("Age-related screening: Annual comprehensive metabolic panel recommended.")

        if disease == "diabetes":
            recs.append("Diabetes-specific: Regular foot exams, eye exams, and kidney function tests recommended.")
        elif disease == "heart":
            recs.append("Cardiovascular: Lipid panel and cardiac stress test may be indicated.")

        if not recs:
            recs.append("Maintain current healthy lifestyle. Schedule routine annual check-up.")

        return recs
