from fpdf import FPDF
import os
from datetime import datetime

class HighFidelityPDFGenerator(FPDF):
    def header(self):
        # Draw top accent bar
        self.set_fill_color(0, 102, 204)  # Deep Clinical Blue
        self.rect(0, 0, 210, 15, 'F')
        
        self.set_font('Helvetica', 'B', 15)
        self.set_text_color(255, 255, 255)
        self.cell(0, 5, 'MASTER CLINICAL DIAGNOSTIC REPORT', ln=True, align='C')
        self.ln(10)

    def footer(self):
        self.set_y(-25)
        self.set_font('Helvetica', 'I', 8)
        self.set_text_color(128, 128, 128)
        self.cell(0, 10, f'Generated by Healthcare AI Platform - Page {self.page_no()}', 0, 0, 'C')
        self.ln(5)
        self.set_font('Helvetica', 'B', 7)
        self.cell(0, 5, 'CONFIDENTIAL MEDICAL AUDIT - FOR INFORMATIONAL USE ONLY', 0, 0, 'C')

    def generate(self, data, filename):
        self.add_page()
        
        # Patient & Identity Header
        self.set_font('Helvetica', 'B', 12)
        self.set_text_color(0, 0, 0)
        self.cell(100, 10, f"PATIENT: {data.get('patient_name', 'Unknown')}", ln=0)
        self.set_font('Helvetica', '', 10)
        self.cell(0, 10, f"TIMESTAMP: {data.get('timestamp', str(datetime.now()))}", ln=1, align='R')
        
        self.cell(100, 10, f"TREATING DOCTOR: {data.get('treating_doctor', 'Automated Agent')}", ln=0)
        self.cell(0, 10, f"RECORD ID: {data.get('_id', 'NEW')}", ln=1, align='R')
        self.ln(5)

        # 1. CORE RISK PANEL
        self.set_fill_color(240, 248, 255)
        self.rect(10, self.get_y(), 190, 25, 'F')
        self.set_font('Helvetica', 'B', 14)
        risk = data.get('risk', 'Unknown')
        if risk == 'High': self.set_text_color(200, 0, 0)
        elif risk == 'Moderate': self.set_text_color(200, 150, 0)
        else: self.set_text_color(0, 150, 0)
        
        self.cell(0, 12, f"RISK ASSESSMENT: {risk.upper()}", ln=True, align='C')
        self.set_font('Helvetica', '', 10)
        self.set_text_color(0, 0, 0)
        self.cell(0, 5, f"AI Confidence Score: {round(data.get('confidence', 0)*100, 2)}%", ln=True, align='C')
        self.ln(10)

        # 2. DIAGNOSTIC REASONING
        self.set_font('Helvetica', 'B', 11)
        self.set_fill_color(230, 230, 230)
        self.cell(0, 8, ' CLINICAL INTELLIGENCE & CONSENSUS', ln=True, fill=True)
        self.ln(2)
        self.set_font('Helvetica', '', 10)
        self.multi_cell(0, 6, f"Consensus Diagnosis: {data.get('disease', 'General Evaluation')}")
        
        intelligence = data.get('consensus_intelligence', {})
        if isinstance(intelligence, dict):
            narrative = intelligence.get('narrative', intelligence.get('consensus_rationale', 'Standard clinical protocols applied.'))
            self.set_font('Helvetica', 'I', 9)
            self.multi_cell(0, 6, f"Rationale: {narrative}")
            
            audit = intelligence.get('handwriting_audit')
            if audit:
                self.ln(2)
                self.set_font('Helvetica', 'B', 9)
                self.cell(0, 6, f"Handwriting Audit: {audit.get('verdict')} (Clarity Index: {audit.get('clarity_score')}/100)", ln=True)

        # 3. PHARMACOLOGICAL ANALYSIS
        self.ln(5)
        self.set_font('Helvetica', 'B', 11)
        self.cell(0, 8, ' IDENTIFIED MEDICATIONS', ln=True, fill=True)
        self.ln(2)
        
        meds = data.get('auto_medications', [])
        if not meds:
            self.set_font('Helvetica', '', 10)
            self.cell(0, 6, "No pharmacological interventions detected.", ln=True)
        else:
            for med in meds:
                self.set_font('Helvetica', 'B', 10)
                self.cell(0, 6, f"> {med.get('name', 'Unknown')}", ln=True)
                self.set_font('Helvetica', '', 9)
                self.multi_cell(0, 5, med.get('note', 'No clinical notes provided.'))
                self.ln(1)

        # 4. RECOMMENDATIONS & LIFESTYLE
        self.ln(5)
        self.set_font('Helvetica', 'B', 11)
        self.cell(0, 8, ' PREVENTIVE DIRECTIVES', ln=True, fill=True)
        self.ln(2)
        recs = data.get('recommendations', {})
        if isinstance(recs, dict):
            for cat, items in recs.items():
                if items:
                    self.set_font('Helvetica', 'B', 9)
                    self.cell(0, 6, cat.upper(), ln=True)
                    self.set_font('Helvetica', '', 9)
                    for item in items:
                        self.multi_cell(0, 5, f"- {item}")
                    self.ln(2)

        # 5. LEGAL DISCLAIMER
        self.set_y(-45)
        self.set_font('Helvetica', '', 7)
        self.set_text_color(100, 100, 100)
        disclaimer = "DISCLAIMER: This document was generated via an Automated Clinical Intelligence Engine. The results are probabilistic and must only be interpreted by a licensed physician. Do not start or stop any medication without professional consultation."
        self.multi_cell(0, 4, disclaimer, align='C')

        self.output(filename)
        return filename

# Simple singleton
pdf_generator = HighFidelityPDFGenerator()
